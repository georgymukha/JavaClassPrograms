package com.java.class05;

public class CompoundOperators {
    public static void main(String[] args) {
        int ab = 50;
        int ac = ab++;
        int ad = ++ab;
        System.out.println(ad++);
        System.out.println(++ab);

        System.out.println(ab++);
        System.out.println(ab++);

    }
}
